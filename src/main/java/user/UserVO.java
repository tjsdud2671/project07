package user;

public class UserVO {
	private int idx;
	private String mid;
	private String pwd;
	private String name;
	private String nick;
	private String gender;
	private String phoneNum;
	private int level;
	
	 	

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	@Override
	public String toString() {
		return "UserVO [idx=" + idx + ", mid=" + mid + ", pwd=" + pwd + ", name=" + name + ", nick=" + nick + ", gender="
				+ gender + ", phoneNum=" + phoneNum + ", level=" + level + "]";
	}
	
	
}
